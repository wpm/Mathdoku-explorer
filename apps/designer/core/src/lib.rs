/// Full designer state: the unit of serialization for save files and undo/redo.
///
/// The `solution` field discriminates the two authoring modes:
///
/// - `Some(grid)` — **With Solution**: a fixed Latin Square was generated at
///   creation and cage targets are read off it.
/// - `None` — **Without Solution**: there is no precomputed solution; the author
///   chooses operator *and* target for each cage, filtered by global feasibility.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct State {
    /// Cage structure being designed.
    pub puzzle: Puzzle,
    /// The fixed Latin Square generated when the puzzle was created, or `None`
    /// in Without-Solution mode.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub solution: Option<Puzzle>,
    /// The currently active cell.
    pub active: Cell,
    /// Provisional cages: disjoint from each other and from puzzle cages.
    pub provisional_cages: BTreeSet<Polyomino>,
}

impl State {
    /// Creates a new blank Without-Solution `State` for an *n*×*n* puzzle.
    ///
    /// There is no precomputed solution (`solution` is `None`) and the working
    /// grid is unconstrained.
    ///
    /// # Errors
    /// Returns an error if `n` is invalid for `Puzzle`.
    pub fn new(n: usize) -> Result<Self, String> {
        let puzzle = Puzzle::new(n).map_err(|e| e.to_string())?;
        Ok(Self {
            puzzle,
            solution: None,
            active: Cell::new(0, 0),
            provisional_cages: BTreeSet::new(),
        })
    }

    /// Creates a new blank With-Solution `State` for an *n*×*n* puzzle.
    ///
    /// The `solution` puzzle is a blank `n×n` puzzle; the real Latin Square is
    /// generated by [`new_latin_square`]. This constructor exists for tests and
    /// for transitions that need a With-Solution shape.
    ///
    /// # Errors
    /// Returns an error if `n` is invalid for `Puzzle`.
    pub fn new_with_solution(n: usize) -> Result<Self, String> {
        let puzzle = Puzzle::new(n).map_err(|e| e.to_string())?;
        let solution = Puzzle::new(n).map_err(|e| e.to_string())?;
        Ok(Self {
            puzzle,
            solution: Some(solution),
            active: Cell::new(0, 0),
            provisional_cages: BTreeSet::new(),
        })
    }

    /// Returns the working puzzle state for display.
    ///
    /// Always returns the puzzle's own propagated state — the fills that remain
    /// possible given the cage constraints and all-different. In With-Solution
    /// mode the fixed Latin square is used separately (to highlight the correct
    /// value in green) but does not change the candidate fills shown per cell.
    ///
    /// # Errors
    /// Returns an error if constraint propagation fails.
    pub fn current(&self) -> Result<Puzzle, mathdoku::Error> {
        Ok(self.puzzle.clone())
    }

    /// Switches from Without-Solution to With-Solution by snapshotting the
    /// unique completion into `solution`.
    ///
    /// # Panics
    ///
    /// Never panics in practice: `first.get(cell)` is infallible for cells in a solved puzzle.
    ///
    /// # Errors
    /// Returns [`Error::NotUnique`] if the puzzle does not have exactly one
    /// global completion, or [`Error::Mathdoku`] if propagation fails.
    pub fn fix(&mut self) -> Result<(), Error> {
        let mut solutions = self.puzzle.solutions();
        let first = match solutions.next() {
            Some(Ok(g)) => g,
            Some(Err(e)) => return Err(e.into()),
            None => return Err(Error::NotUnique),
        };
        if solutions.next().is_some() {
            return Err(Error::NotUnique);
        }
        drop(solutions);
        let n = first.n();
        let square: Vec<Vec<N>> = (0..n)
            .map(|r| {
                (0..n)
                    .map(|c| {
                        first
                            .get(Cell::new(r, c))
                            .unwrap_or_default()
                            .values()
                            .first()
                            .copied()
                            .unwrap_or(0)
                    })
                    .collect()
            })
            .collect();
        self.solution = Some(Puzzle::from_latin_square(n, &square)?);
        Ok(())
    }

    /// Switches from With-Solution to Without-Solution by discarding `solution`.
    ///
    /// Always succeeds — the cages were chosen against a known completion and
    /// remain globally feasible after the field is dropped.
    pub fn unfix(&mut self) {
        self.solution = None;
    }
}

// ---- application state ----

/// Mutable backend state owned by the host (native: `Mutex<AppState>`).
///
/// All fields are `None` until a puzzle is created or loaded.
/// `solution` is always kept in sync with `puzzle`
/// by every function that mutates the puzzle.
#[derive(Default)]
pub struct AppState {
    /// Cage structure being designed.
    pub puzzle: Option<Puzzle>,
    /// Latin-square solution fixed at puzzle creation. Singleton values for every cell.
    pub solution: Option<Puzzle>,
    /// Path of the currently open `.mathdoku` file, or `None` if unsaved.
    pub path: Option<String>,
    /// Whether the puzzle has unsaved changes.
    pub dirty: bool,
    /// Last-known active cell, persisted in `last_open.json`.
    /// `None` means (0, 0) (default when no puzzle is loaded or after a fresh load).
    pub active: Option<Cell>,
}

impl AppState {
    /// Assembles a [`State`] from the current fields, or `None` if no puzzle is loaded.
    ///
    /// `solution` is `None` in Without-Solution mode and passes through unchanged.
    /// `provisional_cages` is always empty — provisional state lives only in the frontend.
    fn to_designer_state(&self) -> Option<State> {
        let puzzle = self.puzzle.clone()?;
        Some(State {
            puzzle,
            solution: self.solution.clone(),
            active: self.active.unwrap_or_else(|| Cell::new(0, 0)),
            provisional_cages: BTreeSet::new(),
        })
    }
}

/// On-disk format for `.mathdoku` save files.
///
/// `puzzle` (the cage structure) is always persisted. `solution` (the fixed
/// Latin square) is present only for With-Solution puzzles; Without-Solution
/// puzzles omit the field, which deserializes back to `None`. Existing
/// With-Solution save files carry a non-null `solution` and load unchanged.
#[derive(Serialize, Deserialize)]
pub struct SaveEnvelope {
    pub version: u32,
    pub puzzle: Puzzle,
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub solution: Option<Puzzle>,
}

// ---- puzzle-mutation command bodies ----

/// Stores `new_puzzle` into `state`, marks dirty,
/// and returns the updated designer state. Shared tail of `insert_cage` and
/// `remove_cage_at`.
fn commit_puzzle(state: &mut AppState, new_puzzle: Puzzle) -> Result<State, Error> {
    state.puzzle = Some(new_puzzle);
    state.dirty = true;
    state.to_designer_state().ok_or(Error::NoPuzzle)
}

/// Creates a new empty *n*×*n* Without-Solution puzzle with no cages and no
/// Latin-square solution.
///
/// `solution` is `None`; the author chooses operator and target for each cage,
/// filtered by global feasibility.
///
/// # Errors
/// Returns an error if `n` is invalid.
pub fn new_empty(state: &mut AppState, n: usize) -> Result<State, Error> {
    let puzzle = Puzzle::new(n)?;
    state.puzzle = Some(puzzle);
    state.solution = None;
    state.path = None;
    state.dirty = true;
    state.to_designer_state().ok_or(Error::NoPuzzle)
}

/// Creates a new puzzle whose solution is a random Latin square drawn from `rng`.
///
/// `solution` holds the fixed Latin-square values (a single value per cell).
/// Taking `rng` rather than calling `rand::rng()` internally gives tests a
/// deterministic-seed knob.
///
/// # Errors
/// Returns an error if `n` is invalid.
pub fn new_latin_square<R: Rng>(
    state: &mut AppState,
    n: usize,
    rng: &mut R,
) -> Result<State, Error> {
    let puzzle = Puzzle::new(n)?;
    let latin = generate_latin_square(n, rng);
    let solution = Puzzle::from_latin_square(n, &latin)?;
    state.puzzle = Some(puzzle);
    state.solution = Some(solution);
    state.path = None;
    state.dirty = true;
    state.to_designer_state().ok_or(Error::NoPuzzle)
}

/// Adds a cage to the current puzzle for the given cells and operator.
///
/// In Without-Solution mode the caller supplies `target` directly. In
/// With-Solution mode `target` is `None` and the value is computed from the
/// `solution` singleton values:
/// - `Given` and single-cell: the cell's solution value.
/// - `Add`: sum of all solution values.
/// - `Multiply`: product of all solution values.
/// - `Subtract` and `Divide` (2-cell only): difference or ratio of the two solution values.
///
/// Returns the updated designer `State`.
///
/// # Errors
/// Returns an error if no puzzle is loaded or `operator` is not valid for
/// the polyomino size.
pub fn insert_cage(
    state: &mut AppState,
    poly: Polyomino,
    operator: Operator,
    target: Option<T>,
) -> Result<State, Error> {
    let puzzle = state.puzzle.as_ref().ok_or(Error::NoPuzzle)?;
    let n = puzzle.n();

    // `Some` in Without-Solution mode (author-chosen target); `None` in
    // With-Solution mode, where the target is derived from the fixed solution.
    // u64 arithmetic avoids intermediate overflow (max product 9^9 = 387M fits T=u32,
    // but partial products during iteration can reach 9^8 * 9 = same bound — still safe,
    // but widening to u64 keeps the arithmetic unambiguous).
    let target: T = if let Some(t) = target {
        t
    } else {
        let cells: Vec<Cell> = poly.iter().copied().collect();
        let true_values: Option<Vec<u64>> = state.solution.as_ref().and_then(|grid| {
            cells
                .iter()
                .map(|&cell| {
                    let v = grid.get(cell).ok()?;
                    v.is_singleton()
                        .then(|| v.values().first().copied().map(u64::from))?
                })
                .collect()
        });
        let raw: u64 = match (&operator, true_values.as_deref()) {
            (Operator::Given, Some(vals)) => vals[0],
            (Operator::Add, Some(vals)) => vals.iter().sum(),
            (Operator::Multiply, Some(vals)) => vals.iter().product(),
            (Operator::Subtract, Some(vals)) => vals[0].abs_diff(vals[1]),
            (Operator::Divide, Some(vals)) => vals[0].max(vals[1]) / vals[0].min(vals[1]),
            _ => 0,
        };
        T::try_from(raw).map_err(|_| mathdoku::Error::InfeasibleCage(poly.clone(), raw))?
    };

    let n = N::try_from(n).map_err(|_| mathdoku::Error::InvalidGridSize(n))?;
    let cage = Cage::new(n, poly, operator, target)?;
    match puzzle.insert_cage(&cage)? {
        Some(new_puzzle) => commit_puzzle(state, new_puzzle),
        None => state.to_designer_state().ok_or(Error::NoPuzzle),
    }
}

/// Removes the cage covering `polyomino` from the current puzzle.
///
/// Returns the updated designer `State`.
///
/// # Errors
/// Returns an error if no puzzle is loaded or no cage covers exactly `polyomino`.
pub fn remove_cage_at(state: &mut AppState, polyomino: &Polyomino) -> Result<State, Error> {
    let puzzle = state.puzzle.as_ref().ok_or(Error::NoPuzzle)?;
    let cage = puzzle
        .get_cage_at(polyomino)
        .ok_or(Error::CageNotFound)?
        .clone();
    match puzzle.remove_cage(&cage)? {
        Some(new_puzzle) => commit_puzzle(state, new_puzzle),
        None => state.to_designer_state().ok_or(Error::NoPuzzle),
    }
}

/// Switches the current puzzle from Without-Solution to With-Solution by
/// snapshotting its unique completion into `solution`.
///
/// # Errors
/// Returns an error if no puzzle is loaded or the puzzle does not have exactly
/// one global completion.
pub fn fix(state: &mut AppState) -> Result<State, Error> {
    let mut designer = state.to_designer_state().ok_or(Error::NoPuzzle)?;
    designer.fix()?;
    state.solution.clone_from(&designer.solution);
    state.dirty = true;
    Ok(designer)
}

/// Switches the current puzzle from With-Solution to Without-Solution by
/// discarding its `solution`.
///
/// # Errors
/// Returns an error if no puzzle is loaded.
pub fn unfix(state: &mut AppState) -> Result<State, Error> {
    let mut designer = state.to_designer_state().ok_or(Error::NoPuzzle)?;
    designer.unfix();
    state.solution = None;
    state.dirty = true;
    Ok(designer)
}

/// Persists the active cell position into `state`.
pub const fn set_active_cell(state: &mut AppState, active: Cell) {
    state.active = Some(active);
}

/// Returns the document state (dirty flag and current file path).
#[must_use]
pub fn get_doc_state(state: &AppState) -> DocState {
    DocState {
        dirty: state.dirty,
        path: state.path.clone(),
    }
}

/// Returns the current designer [`State`], or `None` if no puzzle is loaded.
#[must_use]
pub fn get_puzzle(state: &AppState) -> Option<State> {
    state.to_designer_state()
}

// ---- save/load serialization seam ----

/// Serializes the current `state` into the pretty-printed [`SaveEnvelope`] JSON,
/// using the current [`SAVE_VERSION`] and field layout.
///
/// # Errors
/// Returns [`Error::NoPuzzle`] if no puzzle is loaded, or [`Error::Serde`] if
/// serialization fails.
pub fn serialize_save(state: &AppState) -> Result<String, Error> {
    let puzzle = state.puzzle.as_ref().ok_or(Error::NoPuzzle)?.clone();
    let solution = state.solution.clone();
    let envelope = SaveEnvelope {
        version: SAVE_VERSION,
        puzzle,
        solution,
    };
    to_string_pretty(&envelope).map_err(|e| Error::Serde(e.to_string()))
}

/// Parses a [`SaveEnvelope`] from `json`, version-checks it, and writes the
/// result into `state`.
///
/// On success the puzzle and solution are replaced, `dirty` is cleared, and
/// `active` is reset to `None`. Returns the loaded designer [`State`]. The
/// caller is responsible for recording the file path.
///
/// # Errors
/// Returns [`Error::Serde`] if the JSON is malformed, or
/// [`Error::UnsupportedVersion`] if the save version is not supported.
pub fn apply_loaded(state: &mut AppState, json: &str) -> Result<State, Error> {
    let envelope: SaveEnvelope = from_str(json).map_err(|e| Error::Serde(e.to_string()))?;
    if envelope.version != SAVE_VERSION {
        return Err(Error::UnsupportedVersion(envelope.version));
    }
    state.puzzle = Some(envelope.puzzle);
    state.solution = envelope.solution;
    state.dirty = false;
    state.active = None;
    state.to_designer_state().ok_or(Error::NoPuzzle)
}

