//! Portable core for Mathdoku Designer.
//!
//! Holds [`AppState`], the puzzle-mutation command bodies (as free functions
//! over `&mut AppState`), the serialization seam for save/load, and the serde
//! types shared with the Leptos frontend. The crate carries no dependency on
//! Tauri, so it compiles for both `wasm32-unknown-unknown` and the native
//! target.
//!
//! The native Tauri wrappers in `src-tauri` lock the `Mutex<AppState>` and
//! delegate here; a web build can own `AppState` differently (for example a
//! `thread_local!<RefCell<AppState>>`) and call the same functions, because
//! the free functions take `&mut AppState` and leave synchronization to the
//! caller.

use std::collections::{BTreeSet, HashSet};

use mathdoku::{Cage, Cell, Grid, Operation, Operator, Polyomino, Puzzle, generate_latin_square};
use rand::Rng;
use serde::{Deserialize, Serialize};
use serde_json::{from_str, to_string_pretty};

/// Serialization version written into every `.mathdoku` save file.
/// Increment when the [`SaveEnvelope`] format changes in a breaking way.
pub const SAVE_VERSION: u32 = 1;

// ---- error ----

/// Errors raised by the Designer command bodies.
///
/// Deliberately separate from [`mathdoku::Error`]: the library error describes
/// library-level operations (puzzle construction, polyomino geometry, solver
/// state) and is on track to be a public crates.io surface, whereas these
/// variants (`NoPuzzle`, `UnsupportedVersion`, â€¦) are Designer concerns.
/// `mathdoku::Error` composes in transparently via `#[from]` so `?` works
/// inside command bodies.
#[derive(Debug, thiserror::Error)]
pub enum Error {
    #[error(transparent)]
    Mathdoku(#[from] mathdoku::Error),
    #[error("no puzzle loaded")]
    NoPuzzle,
    #[error("cage not found")]
    CageNotFound,
    #[error("puzzle does not have exactly one completion")]
    NotUnique,
    #[error("serialization failed: {0}")]
    Serde(String),
    #[error("unsupported save version: {0}")]
    UnsupportedVersion(u32),
}

